CREATE TABLE dbo.Comuna
(
    id_comuna     INT         NOT NULL,
    nombre_comuna VARCHAR(40) NOT NULL,
    CONSTRAINT comuna_id_pk PRIMARY KEY CLUSTERED(id_comuna)
);
GO

CREATE TABLE dbo.Estado_civil
(
    id_estado_civil TINYINT     NOT NULL,
    descripcion     VARCHAR(15) NOT NULL,
    CONSTRAINT estado_civil_id_pk PRIMARY KEY CLUSTERED (id_estado_civil ASC),
    CONSTRAINT estado_civil_descripcion_un UNIQUE NONCLUSTERED (descripcion)
);
GO

CREATE TABLE dbo.Marca
(
    id_marca     INT         NOT NULL,
    nombre_marca VARCHAR(40) NOT NULL,
    CONSTRAINT marca_id_pk PRIMARY KEY CLUSTERED (id_marca ASC),
    CONSTRAINT marca_nombre_marca_un UNIQUE (nombre_marca)
);
GO

CREATE TABLE dbo.Modelo
(
    id_modelo     INT         NOT NULL,
    nombre_modelo VARCHAR(40) NOT NULL,
    CONSTRAINT modelo_id_pk PRIMARY KEY CLUSTERED(id_modelo ASC)
);
GO

CREATE TABLE dbo.Marca_modelo
(
    id_marca  INT NOT NULL,
    id_modelo INT NOT NULL,
    CONSTRAINT marca_modelo_pk PRIMARY KEY CLUSTERED(id_marca, id_modelo),
    CONSTRAINT marca_modelo_marca_fk FOREIGN KEY (id_marca)
    REFERENCES dbo.Marca (id_marca),
    CONSTRAINT marca_modelo_modelo_fk FOREIGN KEY (id_modelo)
    REFERENCES dbo.Modelo (id_modelo)
);
GO

CREATE TABLE dbo.Region
(
    id_region     TINYINT     NOT NULL,
    nombre_region VARCHAR(50) NOT NULL,
    CONSTRAINT region_id_pk PRIMARY KEY CLUSTERED(id_region ASC)
);
GO

CREATE TABLE dbo.Region_comuna
(
    id_region TINYINT NOT NULL,
    id_comuna INT     NOT NULL,
    CONSTRAINT region_comuna_pk PRIMARY KEY CLUSTERED(id_region, id_comuna),
    CONSTRAINT region_comuna_region_fk FOREIGN KEY (id_region)
    REFERENCES dbo.Region (id_region),
    CONSTRAINT region_comuna_comuna_fk FOREIGN KEY (id_comuna)
    REFERENCES dbo.Comuna (id_comuna)
);
GO

CREATE TABLE dbo.Sexo
(
    id_sexo     TINYINT     NOT NULL,
    descripcion VARCHAR(10) NOT NULL,
    CONSTRAINT sexo_id_pk PRIMARY KEY CLUSTERED (id_sexo ASC),
    CONSTRAINT sexo_descripcion_un UNIQUE NONCLUSTERED (descripcion)
);
GO

CREATE TABLE dbo.Tipo_contrato
(
    id_tipo_contrato TINYINT     NOT NULL,
    descripcion      VARCHAR(15) NOT NULL,
    CONSTRAINT tipo_contrato_id_pk PRIMARY KEY CLUSTERED(id_tipo_contrato ASC),
    CONSTRAINT tipo_contrato_descripcion_un UNIQUE NONCLUSTERED(descripcion)
);
GO


CREATE TABLE dbo.Plan_seguro
(
    id_plan            INT          NOT NULL,
    id_tipo_contrato   TINYINT      NOT NULL,
    numero_plan        VARCHAR(5)   NOT NULL,
    nombre_plan        VARCHAR(30)  NOT NULL,
    prima_base_plan    DECIMAL(6,2) DEFAULT 0 NOT NULL,
    poliza_actual_plan VARCHAR(15)  NOT NULL,
    CONSTRAINT plan_id_pk PRIMARY KEY CLUSTERED(id_plan),
    CONSTRAINT plan_numero_plan_un UNIQUE NONCLUSTERED (numero_plan),
    CONSTRAINT plan_tipo_contrato_fk FOREIGN KEY (id_tipo_contrato)
    REFERENCES dbo.Tipo_contrato (id_tipo_contrato)
);
GO

CREATE TABLE dbo.Cliente
(
    id_cliente        BIGINT      NOT NULL IDENTITY(1,1),
    id_sexo           TINYINT     NOT NULL,
    id_estado_civil   TINYINT     NOT NULL,
    rut_cliente       VARCHAR(10) NOT NULL,
    nombre_cliente    VARCHAR(20) NOT NULL,
    APELLIDOS_CLIENTE VARCHAR(50) NOT NULL,
    fecnac_cliente    DATE        NOT NULL,
    CONSTRAINT cliente_id_pk PRIMARY KEY CLUSTERED (id_cliente ASC),
    CONSTRAINT cliente_rut_un UNIQUE NONCLUSTERED(rut_cliente),
    CONSTRAINT cliente_sexo_fk FOREIGN KEY (id_sexo)
    REFERENCES dbo.Sexo (id_sexo),
    CONSTRAINT cliente_estado_civil_fk FOREIGN KEY (id_estado_civil)
    REFERENCES dbo.Estado_civil (id_estado_civil)
);
GO

CREATE TABLE dbo.contrato
(
    id_contrato                      BIGINT       IDENTITY(1,1) NOT NULL,
    id_cliente                       BIGINT       NOT NULL,
    id_plan                          INT          NOT NULL,
    numero_contrato                  VARCHAR(14)  NOT NULL,
    fecha_contrato                   DATE         NOT NULL,
    fecha_termino_contrato           DATE,
    inicio_vigencia_contrato         DATE,
    termino_vigencia_contrato        DATE,
    esta_vigente_contrato            BIT          DEFAULT 0 NOT NULL,
    tiene_declaracion_salud_contrato BIT          DEFAULT 0 NOT NULL,
    prima_anual_contrato             DECIMAL(6,2) DEFAULT 0 NOT NULL,
    observacion_contrato             VARCHAR(250),
    prima_mensual_contrato           DECIMAL(6,2) DEFAULT 0 NOT NULL,
    CONSTRAINT contrato_id_pk PRIMARY KEY CLUSTERED(id_contrato ASC),
    CONSTRAINT contrato_numero_contrato_un UNIQUE NONCLUSTERED(numero_contrato),
    CONSTRAINT contrato_cliente_fk FOREIGN KEY (id_cliente) 
    REFERENCES dbo.Cliente (id_cliente),
    CONSTRAINT contrato_plan_fk FOREIGN KEY (id_plan)
    REFERENCES dbo.Plan_seguro (id_plan)
);
GO

CREATE TABLE dbo.vehiculo
(
    id_vehiculo      BIGINT     IDENTITY(1,1) NOT NULL,
    id_modelo        INT        NOT NULL,
    patente_vehiculo VARCHAR(6) NOT NULL,
    anio_vehiculo    INT        NOT NULL,
    CONSTRAINT vehiculo_id_pk PRIMARY KEY CLUSTERED(id_vehiculo ASC),
    CONSTRAINT vehiculo_modelo_fk FOREIGN KEY (id_modelo)
REFERENCES dbo.Modelo (id_modelo)
);
GO

CREATE TABLE dbo.Vivienda
(
    id_vivienda              BIGINT       IDENTITY(1,1) NOT NULL,
    id_comuna                INT          NOT NULL,
    codigo_postal_vivienda   VARCHAR(8)   NOT NULL,
    anio_vivienda            INT          NOT NULL,
    direccion_vivienda       VARCHAR(50)  NOT NULL,
    valor_inmueble_vivienda  DECIMAL(9,2) DEFAULT 0 NOT NULL,
    valor_contenido_vivienda DECIMAL(9,2) DEFAULT 0 NOT NULL,
    CONSTRAINT vivienda_id_pk PRIMARY KEY CLUSTERED(id_vivienda),
    CONSTRAINT vivienda_cona_fk FOREIGN KEY (id_comuna)
    REFERENCES dbo.Comuna (id_comuna)
);
GO

CREATE TABLE dbo.Contrato_vehiculo
(
    id_contrato BIGINT NOT NULL,
    ID_VEHICULO BIGINT NOT NULL,
    CONSTRAINT contrato_vehiculo_pk PRIMARY KEY CLUSTERED(id_contrato, id_vehiculo),
    CONSTRAINT contrato_vehiculo_contrato_fk FOREIGN KEY (id_contrato)
    REFERENCES dbo.Contrato (id_contrato),
    CONSTRAINT contrato_vehiculo_vehiculo_fk FOREIGN KEY (id_vehiculo)
    REFERENCES dbo.Vehiculo (id_vehiculo)
);
GO

CREATE TABLE dbo.Contrato_vivienda
(
    id_contrato BIGINT NOT NULL,
    id_vivienda BIGINT NOT NULL,
    CONSTRAINT contrato_vivienda_pk PRIMARY KEY CLUSTERED(id_contrato, id_vivienda),
    CONSTRAINT contrato_vivienda_contrato_fk FOREIGN KEY (id_contrato)
    REFERENCES dbo.contrato (id_contrato),
    CONSTRAINT contrato_vivienda_vivienda_fk FOREIGN KEY (id_vivienda)
    REFERENCES dbo.Vivienda (id_vivienda)
);
GO



